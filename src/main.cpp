void ensureMqttConnected() {
    if (!WiFi.isConnected()) {
        return; // No WiFi ‚Äî skip
    }

    if (!mqttClient.connected()) {
        unsigned long now = millis();
        if (now - mqttLastConnectAttempt >= mqttReconnectInterval) {
            printAll("üîÑ Attempting MQTT connect to %s:%u ...\n", mqttHost, mqttPort);

            bool connected = false;
            if (strlen(mqttUser) > 0) {
                connected = mqttClient.connect(engine_name.c_str(), mqttUser, mqttPass);
            } else {
                connected = mqttClient.connect(engine_name.c_str());
            }

            if (connected) {
                printAll("‚úÖ MQTT connected.");
            } else {
                printAll("‚ö†Ô∏è MQTT connect failed (state=%d)\n", mqttClient.state());
            }

            mqttLastConnectAttempt = now;
        }
    }

    // Must call loop always
    mqttClient.loop();
}


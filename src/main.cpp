void syncNTP(Stream &src) {
    // Ensure Wiifi is connected
    if (!WiFi.isConnected()) {
        src.println("‚ùå WiFi not connected. Cannot sync NTP.");
        return;
    }
    time_t now = time(nullptr);
    // If time already synced (example > 2024)
    if (now > 1700000000) {  // ~Jan 2024 timestamp
        src.println("‚è≥ Already synced ‚Äî skip resync.");
        return;
    }

    struct tm timeinfo;
    bool synced = false;

    for (int i = 0; i < MAX_NTP; i++) {
        if (strlen(ntpList[i]) == 0) continue;

        configTime(3600 * 7, 0, ntpList[i]); // Example +7 timezone
        delay(2000);  // wait for NTP

        if (getLocalTime(&timeinfo)) {
            src.printf("‚úÖ NTP sync OK: %s\n", ntpList[i]);
            synced = true;
            ntpSynced = true;
            break;
        } else {
            src.printf("‚ö†Ô∏è NTP failed: %s\n", ntpList[i]);
        }
    }

    // if (!synced) {
    //     src.println("‚ùå All NTP failed.");
    //     ntpSynced = false;
    // }

    // Added on June 25,2025 -- To support NTP in-case of no wifi (prolonged power outage)
    if (synced) {
        lastNtpSyncEpoch = time(nullptr);
        ntpAvailable = true;
        prefs.begin("ntp", false);
        prefs.putUInt("lastsync", lastNtpSyncEpoch);
        prefs.end();
        src.printf("‚úÖ NTP sync successful. Last sync: %s\n", ctime(&lastNtpSyncEpoch));
        printAll("‚úÖ NTP sync successful. Last sync: ");
    } else {
        ntpAvailable = false;
        printAll("‚ùå NTP sync failed.");
    }
    
}

void checkNtpStatus(Stream &out) {
  uint32_t nowEpoch = millis() / 1000; // uptime in seconds

  if (!ntpAvailable) {
    out.println("‚ö†Ô∏è  NTP not available. Using internal time.");
  }

  if (lastNtpSyncEpoch == 0) {
    out.println("‚ùå No NTP sync history found.");
    return;
  }

  uint32_t timeSinceSync = nowEpoch - (lastNtpSyncEpoch % 0xFFFFFFFF);
  out.printf("üïí Last NTP sync was %lu sec ago (%lu min)\n", timeSinceSync, timeSinceSync / 60);

  if (timeSinceSync < 3600) {
    out.println("‚úÖ Time is accurate (synced < 1 hour ago).");
  } else if (timeSinceSync < MAX_NTP_AGE_SEC) {
    out.println("‚ö†Ô∏è  Time may drift (last sync > 1 hour).");
  } else {
    out.println("‚ùó NTP sync too old (> 1 day) ‚Äî risk of drift.");
  }
}


